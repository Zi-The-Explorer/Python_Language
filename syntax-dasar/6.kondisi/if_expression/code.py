# kondisi if adalah kondisi yang akan di eksekusi jika kondisi yang ditentukan benar atau True
 
nilai = 100

# Jika nilai pada kondisi bernilai true maka program di bawah nya akan dijalankan 
if nilai > 80:
    print("nilai lebih besar dari 80 ") 
    
    
    
    
# Jika nilai pada kondisi bernilai false maka program di bawah nya tidak akan dijalankan
if nilai > 90:
    print("nilai lebih besar dari 90")