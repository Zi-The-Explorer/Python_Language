# Penjumlahan 
print(13 + 2)
apel = 7
jeruk = 9
buah = apel + jeruk
print(buah)

# Pengurangan 
print(13 - 2)
hutang = 10000
bayar = 5000
sisa_hutang = hutang - bayar
print ("Sisa hutang anda adalah", sisa_hutang)

# perkalian
print(13 * 2)
panjang = 10
lebar = 5
luas = panjang * lebar
print("Luas persegi panjang adalah", luas)

# Pembagian
print(13 / 2)
kue = 10
anak = 3
kue_per_anak = kue / anak
print ("Kue per anak adalah", kue_per_anak)

# Modulus / Sisa bagi
print(13 % 2)
bilangan1 = 10
bilangan2 = 3   
hasil = bilangan1 % bilangan2
print("Sisa bagi dari", bilangan1, "dan", bilangan2, "adalah", hasil)

# Pembagian bulat 
print(13 // 2)
